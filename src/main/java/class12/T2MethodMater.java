public class T2MethodMater {

   //create a method that will take a number and prints whether
    //the number is even or odd number
    void evenOddNumbers(int num1){
        if (num1%2==0){
            System.out.println(num1+ "Number is even");
        }else{
            System.out.println(num1+ "Number is odd");
        }

    }
}
