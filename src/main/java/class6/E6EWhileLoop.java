package class6;

public class E6EWhileLoop {
    public static void main(String[] args) {
        int i = 10; //this code is to decrease
        while (i>=1) {
            System.out.println(i);
           i--; //this decreases the number by 1
           i--;
            i-=2; // this decreases the number by 2
        }
    }
}
