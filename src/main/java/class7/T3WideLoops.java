package class7;

public class T3WideLoops {
    public static void main(String[] args) {
        int i = 0;
        while (i<4) {

            int j = 0;
            while (j<7) {
                System.out.print(j+" ");
                j++;
            }
            System.out.println();
            i++;
        }
    }
}
