package class2;

public class E10Shortcuts {
    public static void main(String[] args) {
        int number=20;
        number=number+10; //take the previous value of number add 10 to it store the results back to number
        System.out.println(number);

        int no1=10;
        no1+=50;
        System.out.println(no1);

        int times=10;
        times*=5;
        System.out.println(times);

        int remove=50;
        remove-=10;
        System.out.println(remove);

        int divi=20;
        divi/=10;
        System.out.println(divi);







    }
}
